# writa a program to demonstrate the math built in fumction 

import math

x=int(input("enter the value of x"))
y=int(input("enter the value of y"))
print("The sum of two number is",math.fsum((x,y)))
print("The factorial of x is",math.factorial(x))
print("The square root of x is",math.sqrt(x))
print("The power of x to y is",math.pow(x,y))
print("The gcd of the two number is",math.gcd(x,y))